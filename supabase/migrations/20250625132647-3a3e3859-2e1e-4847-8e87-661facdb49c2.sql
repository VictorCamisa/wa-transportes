  END AS empresa,
  CASE (ROW_NUMBER() OVER() % 4)
    WHEN 0 THEN 'PATRICIA'
    WHEN 1 THEN 'GUSTAVO'
    WHEN 2 THEN 'WILLIAM'
    ELSE 'MICHELLE'
  END AS solicitante,
  CASE (ROW_NUMBER() OVER() % 3)
    WHEN 0 THEN 'BANCO'
    WHEN 1 THEN 'CORREIO'
    ELSE 'FARMÁCIA'
  END AS servico,
  CASE (ROW_NUMBER() OVER() % 2)
    WHEN 0 THEN 'TAUBATÉ'
    ELSE 'SJCAMPOS'
  END AS cidade,
  'MOTO' AS tipo_veiculo,
  'MOTO' AS veiculo,
  CASE (ROW_NUMBER() OVER() % 3)
    WHEN 0 THEN 'FICO'
    WHEN 1 THEN 'LE'
    ELSE 'RAFAEL'
